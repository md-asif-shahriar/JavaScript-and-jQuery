// normal if else
let num = 5;

if(num > 0){
    console.log("Positive");
}   
else if(num < 0){
    console.log("Negative");
}   
else{
    console.log("Zero");
}

//switch case
let num1 = 10;

switch(num1){
    case 1:
        console.log("Positive");
        break;
    case -1:
        console.log("Negative");
        break;  
    default:
        console.log("Zero");
}